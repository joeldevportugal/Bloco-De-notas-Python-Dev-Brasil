# Aqui Vamos defenir A biblioteca Que vamos Usar -------------------------------------------------------
import tkinter as tk # Aqui vamos Importar a Blibioteca Tkinter Para Adicionar Menus 
import customtkinter # Aqui vamos Importar a Biblioteca Para O nosso Script 
#-------------------------------------------------------------------------------------------------------







# aqui vamos configurar a Janela ----------------------------------------------------------------------
#------------------------------------------------------------------------------------------------------
# Aqui chamamos a Biblioteca Customtkinter e criamos A janela
Janela = customtkinter.CTk() 
#------------------------------------------------------------------------------------------------------
#------------------------------------------------------------------------------------------------------
# Aqui Vamos Defenir A Geometria Para a Nossa Janela
Janela.geometry('860x700+100+100')
#------------------------------------------------------------------------------------------------------
#------------------------------------------------------------------------------------------------------
# Aqui Vamos Defenir Se o Nosso Utilizador pode Ou nao Redmensionar a janela
Janela.resizable(False, False)
#------------------------------------------------------------------------------------------------------
#------------------------------------------------------------------------------------------------------
# Aqui Vamos defenir O Nosso Titulo Da nossa Aplicação 
Janela.title('Bloco Notas Py Dev Brasil Dev joel 2024 ©')
#------------------------------------------------------------------------------------------------------
#------------------------------------------------------------------------------------------------------
# Aqui Vamos criar o Nosso Frontend ------------------------------------------------------------------- 

# Criando o menu --------------------------------------------------------------------------------------
menubar = tk.Menu(Janela) # aqui vamos adicionar O menu Ao nosso Master Que é a Nossa Janela
Janela.config(menu=menubar) # aqui Configurar O nosso Menu Na Nossa Janela  
#-----------------------------------------------------------------------------------------------------
# Adicionando itens ao menu---------------------------------------------------------------------------
# vamos adicionar O item Menu e configurar a fonte
menu_arquivo = tk.Menu(menubar, tearoff=0) # Vamos  configurar o menu na nossa Janela
menubar.add_cascade(label="Menu", menu=menu_arquivo, font=('Arial 15')) 
#------------------------------------------------------------------------------------------------------
# Adicionando subitens ao menu "Arquivo"---------------------------------------------------------------
#------------------------------------------------------------------------------------------------------
# vamos adicionar um Submenu e configurar a fonte
menu_arquivo.add_command(label="Abrir", font=('Arial 15')) 
#------------------------------------------------------------------------------------------------------
#------------------------------------------------------------------------------------------------------
# vamos adicionar um Submenu e configurar a fonte
menu_arquivo.add_command(label="Guardar", font=('Arial 15')) 
#------------------------------------------------------------------------------------------------------
#------------------------------------------------------------------------------------------------------
# vamos adicionar um Submenu e configurar a fonte
menu_arquivo.add_command(label="Sair", font=('Arial 15'))
#------------------------------------------------------------------------------------------------------
#-------------------------------------------------------------------------------------------------------
#------------------------------------------------------------------------------------------------------
# Aqui Vamos Criar uma ListBox e colocar dentro Do master e configurar A alrura e Lagura e defenir a font
Ltexto = tk.Listbox(Janela, font=('Arial 16'), width=89, height=35) 
#-------------------------------------------------------------------------------------------------------
#-------------------------------------------------------------------------------------------------------
# Aqui Vamos Posicionar a Nossa Listbox 
Ltexto.place(x=0, y=0)
#------------------------------------------------------------------------------------------------------
#------------------------------------------------------------------------------------------------------
#------------------------------------------------------------------------------------------------------
# Aqui importamos O comando para iniciar a Janela -----------------------------------------------------
Janela.mainloop() # Aqui Vamos Iniciar a Nosssa Janela
#------------------------------------------------------------------------------------------------------
